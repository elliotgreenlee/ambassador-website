<section class="two-thirds column">
  <h1>Frequently Asked Questions</h1>

  <h2>Q. What is the benefit of the Engineering Professional Practice Program?</h2>
  <p>Co-oping is highly encouraged. Work experience is invaluable in many aspects of
    academic life. First, it aids in the learning process. Seeing firsthand what the
    student will work on in class helps develop a better sense of the dynamics of what
    is going on. Second, it helps in developing the student professionally. There are
    trade secrets that one can learn in the industry that cannot be taught in the
    classroom. Thirdly, it helps the student evaluate if the career path they have
    chosen is a good fit or their personality. Engineering the profession and
    engineering the degree are vastly different. It is also invaluable during the
    interview process.The student has so much more to bring to the table with real
    work experience. Lastly, the money helps pay for school. For more information,
    visit the <a href="http://www.coop.utk.edu/" target="_blank">Engineering Professional Practice</a>
    website.
  </p>

  <h2>Q. What is the Engage Engineering Fundamentals Program?</h2>
  <p>The Engage Program is an unique freshman engineering program offered at the
    University of Tennessee. The program is based on team-based activities and projects
    combined with teaching the basics dynamics and statics. Visit the
    <a href="http://www.engr.utk.edu/efd/" target="_blank">Engage Program</a> website for more
    information.
  </p>

  <h2>Q. What is the Engage Learning Community?</h2>
  <p>The Engage Learning Community is one of several parts of the Engage Programs.
    The learning community consists of a couple of floors reserved especially for
    engineering students in Morrill Hall. Staying in the same floor, the students
    in the Engage community become part of a group that is able to help each other
    during exams, as well as bond as future engineers. Of course, there are also some
    great reasons to join. The communities have events, such as whitewater rafting,
    paint ball tournaments and trips to Six Flags in Atlanta.
  </p>

  <h2>Q. How do I become a part of the Engage Program?</h2>
  <p>All freshmen who are admitted into the College of Engineering are automatically
    in the Engage Program.
  </p>

  <h2>Q. Do I have to live in the Engage Learning Community to be a part of the
    Engage Program?
  </h2>
  <p>No. Being a part of the Learning Community in Morrill Hall is voluntary. You
    will be a part of the Engage Program whether you live there or in any other dorm
    on campus.
  </p>
              
  <h2>Q. What is the Engineering Honors Program?</h2>
  <p>The Engineering Honors Program is independent of (but complementary to) the
    university's Chancellors Honors Program (CHP), and an engineering student may
    choose to pursue both programs. In addition, the College 	administers two
    additional honors programs in cooperation with the National Academy of Engineering,
    the Grand Challenge Engineer (GCE) program and the Grand Challenge Scholar (GCS)
    program. For more information, visit the
    <a href="http://www.engr.utk.edu/academics/honors.html" target="_blank">Engineering Honors Program</a>
    website.
  </p>

  <h2>Q. How do I become a part of the Engineering Honors Program?</h2>
  <p>Admission as a first year student to the EHP/HC is by invitation, which is extended
    by the Dean's office to students meeting rigorous academic standards in their high
    school coursework and to all students accepted into the Chancellors Honors Program.
    Recent entering classes have had an average high school core GPA of above 4.0 and
    an average composite ACT of 33.
  </p>

  <h2>Q. Where can I go to see student evaluations of professors?</h2>
  <p>The University of Tennessee asks students to evaluate professors at the end of
    each semester. All updated evaluations of professors can be found on the
    <a href="https://webappsportal.dii.utk.edu/tn101ReportViewer/" target="_blank">TN101 Online</a>
    website.
  </p>

  <h2>Q. What is the best meal plan option?</h2>
  <p>It all depends on how often and when you plan to eat. The Unlimited Plan allows
    you to eat 3 meals per day every day of the week that a cafeteria is open. The Any
    Ten Plan gives you 10 meals per week that do not roll over from week to week. Think
    about how often you plan to eat in the cafeteria, and make your decision. If you
    will be purchasing snacks often from the convenience stores, you may want to choose
    a plan with more bonus bucks. Check out <a href="http://www.utdining.com/" target="_blank">
    UT Dining Services Website</a>
  </p>

  <h2>Q. What are Bonus Bucks?</h2>
  <p>As stated on the UT Dining Serves Website Bonus Bucks are provided as part of
    your meal membership. Bonus Bucks may be used at all Dining Services restaurants
    and convenience stores on campus. Bonus Bucks add convenience and variety to the
    already extensive offerings available with your meal membership. Bonus Bucks can
    be used to purchase goods at the many convenience stores around campus. For more
    questions on Bonus Bucks or meal plan in general, visit
    <a href="http://www.utdining.com/" target="_blank">UT Dining Services Website.</a>
  </p>

  <h2>Q. How many hours per week should I plan to attend classes and studying?</h2>
  <p>If you register for 14 credit hours of classes, you can expect to spend 14
    hours per week in class.This does not include time required to spend doing homework
    and projects. Typically, professors expect students to spend 2 hours of work
    outside the classroom for every 1 credit hour in the classroom. This will vary
    depending on the class and time of semester. Students registered for 15 credit
    hours can expect to spend between 20 and 30 hours per week in class and completing
    assignments.
  </p>

  <h2>Q. How do I know if engineering is right for me?</h2>
  <p>The engineering fundamentals program that UT offers is designed to provide basic
    knowledge used by engineers and many other disciplines. If you do decide during
    your first year that engineering is not for you, many of the courses that you will
    be taking will count as basic requirements for several other majors. That means
    that you won't be far behind if you do change majors during or after your first
    year! Remember, even if you are not sure of what type of engineering to pursue,
    the principles you'll learn during the first year in Engineering will be carried
    with you through your entire college career.
  </p>

  <h2>Q. How long can I wait to decide which type of Engineering I want to do?</h2>
  <p>Through the Engineering Fundamentals Program, students are
    exposed to the various types of Engineering disciplines. This ensures that students
    are well informed about what a particular type of Engineering may do on a day to
    day basis. Students are encouraged to investigate the majors we offer and decide on
    one during their first year. You can learn more about the different engineering
    majors at the 
    <a href="http://www.engr.utk.edu/academics/" target="_blank">Academic Programs</a>
    website.
  </p>

